num_1 = 3
num_2 = 4
num_3 = -5
print(type(num_1))
# to see the type of data.

# ArithmeticOperators
print(num_1 + num_2)
print(num_1 - num_2)
print(num_1 * num_2)
print(num_1 / num_2)
print(num_1 // num_2)  # floor division(gives up decimals and does rounding)
print(num_1 ** num_2)  # exponential operator
print(num_1 % num_2)  # gives a remainder

print(abs(num_3))  # The absolute value of num_3.

print(round(num_1 / num_2))  # rounding off the value.
